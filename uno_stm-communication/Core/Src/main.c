void HAL_UARTEx_RxEventCallback(UART_HandleTypeDef *huart,
                                uint16_t Size)
{
    if (huart->Instance == LPUART1)
    {
        if(Size < RX_BUFFER_SIZE)
        {
            lp_rx_buffer[Size] = '\0';
        }
        else
        {
            lp_rx_buffer[RX_BUFFER_SIZE - 1] = '\0';
        }

        printf("[RX from Uno]: %s\r\n",
               (char*)lp_rx_buffer);

        ProcessCommand((char*)lp_rx_buffer);

        HAL_UARTEx_ReceiveToIdle_IT(
            &hlpuart1,
            lp_rx_buffer,
            RX_BUFFER_SIZE);
    }
}

void ProcessCommand(char *cmd)
{
    char tx_buf[64];

    cmd[strcspn(cmd,"\r\n")] = '\0';

    printf("RX: %s\r\n",cmd);

    if(strcmp(cmd,"<GET,FREQ>")==0)
    {
        sprintf(tx_buf,
                "<FREQ,%lu>\r\n",
                lora.frequency);
    }

    else if(strcmp(cmd,"<GET,SF>")==0)
    {
        sprintf(tx_buf,
                "<SF,%d>\r\n",
                lora.sf);
    }

    else if(strcmp(cmd,"<GET,BW>")==0)
    {
        sprintf(tx_buf,
                "<BW,%lu>\r\n",
                lora.bw);
    }

    else if(strcmp(cmd,"<GET,CR>")==0)
    {
        sprintf(tx_buf,
                "<CR,%d>\r\n",
                lora.cr);
    }

    else if(strcmp(cmd,"<GET,POWER>")==0)
    {
        sprintf(tx_buf,
                "<POWER,%d>\r\n",
                lora.tx_power);
    }

    else
    {
        sprintf(tx_buf,
                "<ERROR>\r\n");
    }

    HAL_UART_Transmit(
        &hlpuart1,
        (uint8_t*)tx_buf,
        strlen(tx_buf),
        HAL_MAX_DELAY);

    printf("TX: %s",tx_buf);
}

